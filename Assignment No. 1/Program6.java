// Even or Odd (if - else)

public class Program6 
{
    public static void main(String[]Arg)
    {
        int a = 35;

        System.out.println("Value of a is : "+a);

        if((a % 2) == 0)
        {
            System.out.println("It is Even number");
        }
        else
        {
            System.out.println("It is Odd number");
        }
    }
}

